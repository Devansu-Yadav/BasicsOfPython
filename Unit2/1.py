# Program to demonstrate the various kind of operations that can be applied to the string.
s="hello World"

print(s.capitalize())
#It converts first letter of the string to captial letter. Without affecting the original value of the stirng.

print(s.center(24))
# The center() method returns a string padded with specified fillchar. It doesn't modify the original string.

print(s.casefold()) 
#The casefold() method is removes all case distinctions present in a string. It is used for caseless match
